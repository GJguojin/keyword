package com.gj.tool.position;

public class KeywordSearchException extends Exception {

	private static final long serialVersionUID = 1L;

	public KeywordSearchException() {
		super();
	}

	public KeywordSearchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public KeywordSearchException(String message, Throwable cause) {
		super(message, cause);
	}

	public KeywordSearchException(String message) {
		super(message);
	}

	public KeywordSearchException(Throwable cause) {
		super(cause);
	}

}
